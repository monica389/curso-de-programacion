# Crea una lista vacía, luego agrega 5 elementos usando append,, imprime la longitud de la lista, elimina el ultimo elemento usando pop y un elemento especifico usando remove 
mi_lista = []

# Agregar 5 elementos usando append
mi_lista.append(10)
mi_lista.append(20)
mi_lista.append(30)
mi_lista.append(40)
mi_lista.append(50)

# Imprimir la longitud de la lista
print("Longitud de la lista:", len(mi_lista))

# Eliminar el último elemento usando pop
mi_lista.pop()

# Eliminar un elemento específico usando remove
mi_lista.remove(20)

# Imprimir la lista final
print("Lista final:", mi_lista)

